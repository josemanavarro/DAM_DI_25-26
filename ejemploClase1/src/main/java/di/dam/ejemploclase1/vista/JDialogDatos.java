    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        String nombre = this.jTextFieldNombre.getText();
        String apellidos = this.jTextFieldApellidos.getText();
        String curso = this.jTextFieldCurso.getText();
        
        if (nombre.isBlank() || apellidos.isBlank() || curso.isBlank()){
            this.jLabelError.setText("ERROR, alg√∫n campo sin contenido");
        } else {   
            Estudiante e = new Estudiante(nombre, apellidos, curso);
            padre.mostrarDatos(e);
        
            this.dispose();
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

